// nullable string type
export type NString = string | null;

// nullable string type
export type NUString = string | null | undefined;

// nullable number type
export type NNumber = number | null;

// nullable boolean type
export type NBoolean = boolean | null;

// nullable generic type
export type NType<T> = T | null;

export type Id = string;
export type Price = number;
export type Name = string;
